# Create examples to compare two numbers using all relational operators (>, <, ==, !=, >=, <=). Write down the results for different types of numbers.

num1 = int(input("Enter your first number: "))
num2 = int(input("Enter your second number: "))
print()
print(f"num1 is greater than num2: {num1 > num2}")
print(f"num1 is less than num2: {num1 < num2}")
print(f"num1 and num2 are equal: {num1 == num2}")
print(f"num1 and num2 are not equal: {num1 != num2}")
print(f"num1 is greater or equal num2: {num1 >= num2}")
print(f"num1 is less or equal num2: {num1 <= num2}")
# Հայտարարել երկու զանգված: Երկու զանգվածներն էլ նույնն են՝ բացառությամբ որ մի զանգվածը պարունակում է մեկ էլեմենտ ավելի շատ:
# Գտնել այդ էլեմենտը:

ls1 = [1, 2, 3, 4, 5]
ls2 = [1, 2, 3, 4]

for i in range(len(ls1)):
  if ls1[i] not in ls2:
    print(ls1[i])

for j in range(len(ls2)):
  if ls2[j] not in ls1:
    print(ls2[j])
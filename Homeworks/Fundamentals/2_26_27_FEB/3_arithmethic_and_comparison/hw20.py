# Accept two integer inputs from the user and calculate the absolute difference between them using the abs() function.
# Print the result.

int1 = int(input("Enter 1st integer number: "))
int2 = int(input("Enter 2nd integer number: "))

print(f"Standard difference: {int1 - int2}")
print(f"Absolute difference: {abs(int1 - int2)}")